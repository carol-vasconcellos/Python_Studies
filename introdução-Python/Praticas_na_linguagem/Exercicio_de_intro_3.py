''' Receba a largura e a altura de um retângulo e calcule sua área. '''

largura_retangulo = float(input(" Largura do retangulo: "))
altura_retangulo = float(input(" Altura do retangulo: "))

area_retangulo = float(largura_retangulo * altura_retangulo)

print(f"A área do retângulo é: {area_retangulo}")